#!/usr/bin/env node
/**
 * scripts/standards/commit-and-push-siblings.mjs
 *
 * Walks every HDPulseAI sibling repo (everything under
 * C:/Users/admin/development/HDPulseAI/ except this repo, the canonical
 * `E_Credentialing`), and for each one that has a real `.git`:
 *
 *   1. Stages ONLY the standards-related files dropped by
 *      bootstrap-repo.mjs and (for the four EssenWebsites repos) the
 *      site-tier STANDARD.md dropped by write-site-tier-standard.mjs.
 *      Nothing else in the repo is touched. If the developer has
 *      unrelated work in progress, this sweep does not pick it up.
 *
 *   2. Skips the commit cleanly if there are no staged changes for
 *      this repo (idempotent: re-running after a successful sweep is
 *      a no-op).
 *
 *   3. Writes a uniform Conventional Commit message
 *      (chore(standards): adopt HDPulseAI L2 propagation forwarders + …)
 *      that documents what was added, why, and the anti-weakening
 *      attestation per STANDARD.md §4.2.
 *
 *   4. If the repo has an `origin` remote, pushes to its current
 *      branch. If not, the commit lands locally and a notice prints
 *      so the developer can wire the remote later.
 *
 * Repos that are uninitialized scaffolds (no `.git`) get the L2
 * forwarders left in place locally; the next `git init` + `git add -A`
 * will pick them up automatically.
 *
 * Anti-weakening (STANDARD.md §4.2):
 *   - Stages an explicit allow-list of file paths only. Will NOT
 *     batch-add unrelated changes.
 *   - Will NOT amend or force-push.
 *   - Will NOT push to anything other than the current branch on
 *     `origin` (per STANDARD.md and CLAUDE.md governance).
 *
 * Usage:
 *   node scripts/standards/commit-and-push-siblings.mjs [--dry-run] [--no-push]
 */

import { execSync } from "node:child_process";
import { existsSync } from "node:fs";
import { promises as fs } from "node:fs";
import path from "node:path";
import process from "node:process";

const ROOT = "C:/Users/admin/development/HDPulseAI";
const KNOWN_BUCKETS = new Set(["EssenApps", "EssenWebsites"]);
const CANONICAL_REPO_BASENAME = "E_Credentialing";

const STANDARDS_FILES = [
  ".cursor/rules/qa-standard.mdc",
  ".github/copilot-instructions.md",
  "AGENTS.md",
  "CLAUDE.md",
  ".vscode/settings.json",
  // Only relevant in the four EssenWebsites repos; harmless elsewhere
  // because `git add` of a non-existent path is a no-op.
  "docs/qa/STANDARD.md",
];

const COMMIT_SUBJECT =
  "chore(standards): adopt HDPulseAI L2 propagation forwarders (Cursor + Copilot + Claude + AGENTS + VSCode)";

const COMMIT_BODY = `Adopt the HDPulseAI L2 propagation forwarders generated by the
canonical repo (E_Credentialing/scripts/standards/bootstrap-repo.mjs)
so this repository inherits the binding HDPulseAI Comprehensive QA
Test Layer (v1.3.0) regardless of which AI agent the developer is
using:

- .cursor/rules/qa-standard.mdc            Cursor (per-repo override)
- .github/copilot-instructions.md          GitHub Copilot (VSCode)
- AGENTS.md                                tool-agnostic (Codex, Cline, Cody, Continue, Windsurf)
- CLAUDE.md                                Claude Code
- .vscode/settings.json                    Copilot useInstructionFiles + .mdc associations
- docs/qa/STANDARD.md                      site-tier forwarder (EssenWebsites only)

Resolution priority (per docs/standards/README.md in the canonical
repo):

  L1 - in-repo docs/qa/STANDARD.md (if present)
  L2 - per-repo forwarders above
  L3 - ~/.cursor/rules/qa-standard-global.mdc (alwaysApply: true)
  L4 - VSCode user-level settings.json + prompts/qa-standard.prompt.md

This commit makes L2 survive a fresh clone onto a new machine; the
standard previously applied only via the developer's local L3/L4
defaults.

## Anti-weakening attestation (STANDARD.md §4.2)

- No .skip / .todo / .fixme introduced.
- No @ts-expect-error or eslint-disable-next-line introduced.
- No coverage threshold lowered.
- No hard-fail condition downgraded to warning.
- No selector widened, no timeout raised, no error swallowed.
- The forwarders are pure additions (or, where AGENTS.md / CLAUDE.md
  pre-existed with hand-written content, the standards pin is
  *prepended* with a horizontal-rule separator so user content is
  preserved verbatim).
- The site-tier STANDARD.md replaces a STALE full-copy v1.1.0 / v1.2.0
  with a v1.3.0 forwarder; this is a *bump*, never a downgrade. The
  pillar scope-out for B/C/D/P on the marketing-site forwarder is a
  factual scope statement, not a relaxation: those pillars literally
  do not have a surface to test on a public marketing site.

## How to refresh

Re-run from the canonical repo:
  node scripts/standards/bootstrap-repo.mjs --all
  node scripts/standards/write-site-tier-standard.mjs   # for EssenWebsites
  node scripts/standards/commit-and-push-siblings.mjs
`;

function sh(cmd, opts = {}) {
  return execSync(cmd, {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
    ...opts,
  });
}

function shTry(cmd, opts = {}) {
  try {
    return { ok: true, out: sh(cmd, opts) };
  } catch (e) {
    return {
      ok: false,
      out: String(e.stdout ?? "") + String(e.stderr ?? ""),
      err: e,
    };
  }
}

async function findSiblingRepos() {
  const out = [];
  const entries = await fs.readdir(ROOT, { withFileTypes: true });
  for (const e of entries) {
    if (!e.isDirectory()) continue;
    if (e.name.startsWith(".") || e.name === "node_modules") continue;
    const lvl1 = path.join(ROOT, e.name);
    if (!KNOWN_BUCKETS.has(e.name)) {
      out.push(lvl1);
      continue;
    }
    const sub = await fs.readdir(lvl1, { withFileTypes: true });
    for (const s of sub) {
      if (!s.isDirectory()) continue;
      if (s.name.startsWith(".") || s.name === "node_modules") continue;
      out.push(path.join(lvl1, s.name));
    }
  }
  // Drop the canonical repo from the sweep.
  return out.filter((p) => path.basename(p) !== CANONICAL_REPO_BASENAME);
}

async function processRepo(repoPath, { dryRun, noPush }) {
  const repoName = path.basename(repoPath);
  const result = { repo: repoName, repoPath, steps: [] };

  if (!existsSync(path.join(repoPath, ".git"))) {
    result.steps.push("skipped (no .git — uninitialized scaffold)");
    return result;
  }

  const opts = { cwd: repoPath };

  // 1. Detect current branch.
  const branchRes = shTry("git rev-parse --abbrev-ref HEAD", opts);
  if (!branchRes.ok) {
    result.steps.push(`error reading branch: ${branchRes.out.trim()}`);
    return result;
  }
  const branch = branchRes.out.trim();
  result.steps.push(`branch: ${branch}`);

  // 2. Stage only standards files that exist in this repo.
  const existing = STANDARDS_FILES.filter((f) =>
    existsSync(path.join(repoPath, f)),
  );
  if (existing.length === 0) {
    result.steps.push("no standards files present (nothing to do)");
    return result;
  }
  for (const f of existing) {
    const r = shTry(`git add -- "${f}"`, opts);
    if (!r.ok) result.steps.push(`add failed for ${f}: ${r.out.trim()}`);
  }

  // 3. Check whether anything is actually staged for these paths.
  const stagedRes = shTry(
    `git diff --cached --name-only -- ${existing.map((f) => `"${f}"`).join(" ")}`,
    opts,
  );
  const stagedFiles = (stagedRes.out ?? "")
    .split(/\r?\n/)
    .map((s) => s.trim())
    .filter(Boolean);
  if (stagedFiles.length === 0) {
    result.steps.push("nothing staged for standards files (already current)");
    return result;
  }
  result.steps.push(`staged: ${stagedFiles.length} file(s)`);

  if (dryRun) {
    result.steps.push("DRY RUN — would commit + push here");
    // Reset the index for safety in dry-run mode so we don't leave
    // anything staged the developer didn't ask for.
    shTry("git reset HEAD --", opts);
    return result;
  }

  // 4. Commit using a temp file (heredoc-safe on Windows PowerShell).
  const msgPath = path.join(repoPath, ".git", "COMMIT_MSG.tmp");
  await fs.writeFile(
    msgPath,
    `${COMMIT_SUBJECT}\n\n${COMMIT_BODY}`,
    "utf8",
  );
  const commitRes = shTry(`git commit -F "${msgPath}"`, opts);
  shTry(`del "${msgPath}"`, opts); // Windows cleanup; harmless if missing
  await fs.rm(msgPath, { force: true });
  if (!commitRes.ok) {
    result.steps.push(`commit failed: ${commitRes.out.trim().slice(0, 300)}`);
    return result;
  }
  result.steps.push("commit OK");

  // 5. Push, if a remote exists and --no-push is not set.
  if (noPush) {
    result.steps.push("push skipped (--no-push)");
    return result;
  }
  const remoteRes = shTry("git remote get-url origin", opts);
  if (!remoteRes.ok) {
    result.steps.push("no `origin` remote — commit landed locally only");
    return result;
  }
  const pushRes = shTry(`git push origin ${branch}`, opts);
  if (!pushRes.ok) {
    result.steps.push(`push failed: ${pushRes.out.trim().slice(0, 300)}`);
    return result;
  }
  result.steps.push(`push OK (origin/${branch})`);

  return result;
}

async function main() {
  const dryRun = process.argv.includes("--dry-run");
  const noPush = process.argv.includes("--no-push");

  const repos = await findSiblingRepos();
  console.log(
    `Sweeping ${repos.length} sibling repos${dryRun ? " (DRY RUN)" : ""}${
      noPush ? " (NO PUSH)" : ""
    }`,
  );
  console.log("");

  const results = [];
  for (const r of repos) {
    const res = await processRepo(r, { dryRun, noPush });
    results.push(res);
    console.log(`  ${res.repo}`);
    for (const s of res.steps) console.log(`      - ${s}`);
  }

  console.log("");
  const committed = results.filter((r) =>
    r.steps.some((s) => s.startsWith("commit OK")),
  ).length;
  const pushed = results.filter((r) =>
    r.steps.some((s) => s.startsWith("push OK")),
  ).length;
  const noGit = results.filter((r) =>
    r.steps.some((s) => s.startsWith("skipped (no .git")),
  ).length;
  const upToDate = results.filter((r) =>
    r.steps.some(
      (s) =>
        s.startsWith("nothing staged") ||
        s.startsWith("no standards files"),
    ),
  ).length;
  console.log(
    `Done. ${results.length} repos visited; ${committed} committed; ${pushed} pushed; ${upToDate} already current; ${noGit} skipped (no .git).`,
  );
}

main().catch((e) => {
  console.error("FATAL:", e);
  process.exit(1);
});
