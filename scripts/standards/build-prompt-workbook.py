    apply_header(ws, 1, headers)
    ws.row_dimensions[1].height = 30

    for i, (
        sid,
        pid,
        ptype,
        title,
        pillars,
        when,
        outcome,
        body,
    ) in enumerate(PROMPTS, start=2):
        ws.cell(row=i, column=1, value=sid).alignment = WRAP_CENTER
        ws.cell(row=i, column=2, value=pid).alignment = WRAP_CENTER
        ws.cell(row=i, column=3, value=ptype).alignment = WRAP_CENTER
        ws.cell(row=i, column=4, value=title).alignment = WRAP_TOP
        ws.cell(row=i, column=5, value=pillars).alignment = WRAP_TOP
        ws.cell(row=i, column=6, value=when).alignment = WRAP_TOP
        ws.cell(row=i, column=7, value=outcome).alignment = WRAP_TOP
        body_cell = ws.cell(row=i, column=8, value=body)
        body_cell.alignment = WRAP_TOP
        body_cell.font = MONO_FONT

        for col in range(1, 9):
            cell = ws.cell(row=i, column=col)
            cell.border = BORDER
            if col != 8:
                cell.font = BODY_FONT
        # Title bold
        ws.cell(row=i, column=4).font = Font(
            name="Calibri", bold=True, size=11
        )
        # Type color
        type_fill = APPLY_FILL if ptype == "APPLY" else VERIFY_FILL
        ws.cell(row=i, column=3).fill = type_fill
        ws.cell(row=i, column=3).font = Font(
            name="Calibri", bold=True, size=11
        )

        # Approximate row height by body length (60 chars/line @ 14pt).
        line_count = max(
            6,
            (len(body) // 60) + body.count("\n") + 2,
            (len(outcome) // 50) + 2,
        )
        ws.row_dimensions[i].height = min(420, 14 * line_count)

    autosize(
        ws,
        {1: 7, 2: 7, 3: 9, 4: 36, 5: 26, 6: 36, 7: 50, 8: 80},
    )
    ws.freeze_panes = "D2"
    ws.auto_filter.ref = f"A1:H{1 + len(PROMPTS)}"


