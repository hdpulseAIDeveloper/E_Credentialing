def main() -> int:
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)

    wb = Workbook()
    build_readme(wb.active)
    build_sections(wb.create_sheet("Sections"))
    build_all_prompts(wb.create_sheet("All prompts"))
    build_workflows(wb.create_sheet("Workflows"))

    wb.save(OUT_PATH)
    print(f"Wrote {OUT_PATH}")
    print(
        f"  Sections: {len(SECTIONS)}  Prompts: {len(PROMPTS)}  "
        f"Workflows: {len(WORKFLOWS)}"
    )
