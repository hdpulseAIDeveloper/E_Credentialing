SECTION_FILL = PatternFill("solid", fgColor="FFE7EEF7")  # pale navy
APPLY_FILL = PatternFill("solid", fgColor="FFE6F4EA")  # soft green
VERIFY_FILL = PatternFill("solid", fgColor="FFE3F2FD")  # soft blue
ZEBRA_FILL = PatternFill("solid", fgColor="FFF6F8FA")
